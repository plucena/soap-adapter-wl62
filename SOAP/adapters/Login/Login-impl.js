/**
 *  WL.Server.invokeHttp(parameters) accepts the following json object as an argument:
 *  
 *  {
 *  	// Mandatory 
 *  	method : 'get' , 'post', 'delete' , 'put' or 'head' 
 *  	path: value,
 *  	
 *  	// Optional 
 *  	returnedContentType: any known mime-type or one of "json", "css", "csv", "javascript", "plain", "xml", "html"  
 *  	returnedContentEncoding : 'encoding', 
 *  	parameters: {name1: value1, ... }, 
 *  	headers: {name1: value1, ... }, 
 *  	cookies: {name1: value1, ... }, 
 *  	body: { 
 *  		contentType: 'text/xml; charset=utf-8' or similar value, 
 *  		content: stringValue 
 *  	}, 
 *  	transformation: { 
 *  		type: 'default', or 'xslFile', 
 *  		xslFile: fileName 
 *  	} 
 *  } 
 */

function Login(user) {
	var request = "<soap:Envelope xmlns:xsi='http://www.w3.org/2001/XMLSchema-instance' xmlns:xsd='http://www.w3.org/2001/XMLSchema' xmlns:soap='http://schemas.xmlsoap.org/soap/envelope/'><soap:Body><ns2:login xmlns:ns2='http://ws.unasp.com/'><usuario>"+user+"</usuario></ns2:login></soap:Body></soap:Envelope>";
	
	
	var input = {
	    method : 'post',
	    returnedContentType : 'xml',
	    path : 'WS/LoginWS',
	    body : {
	    	content: request,
	    	parameters: [user],
	    	contentType: 'text/xml' 	    		
	    }	
	};
	
	var serviceResponse =  WL.Server.invokeHttp(input);
	return serviceResponse;
	
}